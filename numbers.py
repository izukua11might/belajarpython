a=5
b=4

# penjuamlahan
print ("penjumlahan a + b = ", a+b)

# pegurangan
print ("pengurangan a + b = ", a-b)

# pembagian
print ("pembagian a + b = ", a/b)

# perkalian
print ("perkalian a + b = ", a*b)

# double slash (pembulatan)
print ("slash/pembulatan a + b = ", a//b)

# modulus/sisa bagi
print ("modulus/sisa bagi a + b = ", a%b)

# kombinasi
print ("kombinasi a + b = ", (a+b)**b)

# pangkat
print ("pangkat a + b = ", a**b)